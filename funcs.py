def add(a, b):
  """
  Складывает два числа.

  Args:
    a (int): Первое число.
    b (int): Второе число.

  Returns:
    int: Сумма двух чисел.
  """

  return a + b

def subtract(a, b):
  """
  Вычитает второе число из первого.

  Args:
    a (int): Первое число.
    b (int): Второе число.

  Returns:
    int: Разность двух чисел.
  """

  return a - b